package algo.array;

import org.junit.jupiter.api.DisplayName;
import org.junit.jupiter.api.Test;
import static org.junit.jupiter.api.Assertions.*;

@DisplayName("Test Array/ValidSudoku")
public class ValidSudokuTest {
  @Test
  public void testValid() {
    String[][] valid1 = { { "5", "3", ".", ".", "7", ".", ".", ".", "." },
        { "6", ".", ".", "1", "9", "5", ".", ".", "." }, { ".", "9", "8", ".", ".", ".", ".", "6", "." },
        { "8", ".", ".", ".", "6", ".", ".", ".", "3" }, { "4", ".", ".", "8", ".", "3", ".", ".", "1" },
        { "7", ".", ".", ".", "2", ".", ".", ".", "6" }, { ".", "6", ".", ".", ".", ".", "2", "8", "." },
        { ".", ".", ".", "4", "1", "9", ".", ".", "5" }, { ".", ".", ".", ".", "8", ".", ".", "7", "9" } };
    assertTrue(ValidSudoku.solution(valid1));
  }

  @Test
  public void testInvalid() {
    String[][] invalid1 = { { "5", "3", ".", ".", "7", ".", ".", ".", "." },
        { "6", ".", ".", "1", "9", "5", ".", ".", "." }, { ".", "9", "8", ".", ".", ".", ".", "6", "." },
        { "8", ".", ".", ".", "6", ".", ".", ".", "3" }, { "4", ".", ".", "8", ".", "3", ".", ".", "1" },
        { "7", ".", ".", ".", "9", ".", ".", ".", "6" }, { ".", "6", ".", ".", ".", ".", "2", "8", "." },
        { ".", ".", ".", "4", "1", "9", ".", ".", "5" }, { ".", ".", ".", ".", "8", ".", ".", "7", "9" } };
    String[][] invalid2 = { { "5", "3", ".", ".", "7", ".", ".", ".", "." },
        { "6", ".", ".", "1", "9", "5", ".", ".", "." }, { ".", "9", "8", ".", ".", ".", ".", "6", "." },
        { "8", ".", ".", ".", "6", ".", ".", ".", "3" }, { "4", ".", ".", "8", ".", "3", ".", ".", "1" },
        { "7", ".", ".", ".", "2", ".", ".", ".", "6" }, { ".", "6", ".", ".", ".", ".", "2", "8", "." },
        { ".", ".", ".", "4", "1", "9", ".", ".", "5" }, { ".", ".", ".", ".", "8", ".", ".", "2", "9" } };
    assertFalse(ValidSudoku.solution(invalid1));
    assertFalse(ValidSudoku.solution(invalid2));
  }
}
